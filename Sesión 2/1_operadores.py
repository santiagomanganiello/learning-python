# Crear unprograma que le solicite al usuario dos números enteros 
# y luego imprima por pantalla:
# la suma de ambos números
# la resta de ambos números 
# la multiplicación de ambos números
# la división de ambos números
# el resto

# Solicitar los numeros
num1 = int(input("Ingrese el primer número: "))
num2 = int(input("Ingrese el segundo número: "))

# Imprimir los resultados
print("La suma de ambos números es: ", num1 + num2)
print("La resta de ambos números es: ", num1 - num2)
print("La multiplicación de ambos números es: ", num1 * num2)
print("La división de ambos números es: ", num1 / num2)
print("El resto de ambos números es: ", num1 % num2)